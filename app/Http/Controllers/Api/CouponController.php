    /**
     * Display the specified resource.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $validate = validator(request()->all(), [
            'code' => 'required|min:3',
            'total' => 'required'
        ]);
        if ($validate->fails()) {
            return response()->json(['message' => $validate->errors()->first()], 400);
        }
        $element = Coupon::active()->where(['code' => request()->code, 'consumed' => false])->first();
        if ($element) {
            return response()->json(new CouponLightResource($element), 200);
        }
        return response()->json(['message' => trans('message.coupon_does_not_exist')], 400);
    }

